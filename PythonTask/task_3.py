def mat(argument_one, argument_two):
    print("sum =", argument_one + argument_two)
    print("subtraction =", argument_one - argument_two)
    print("division =", argument_one / argument_two)
    print("multiplication =", argument_one * argument_two)

number_one = int(input("number one: "))
number_two = int(input("number two: "))

mat(number_one, number_two)
